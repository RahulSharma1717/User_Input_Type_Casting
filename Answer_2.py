# Take input of the length and breadth of a rectangle from the user and print the area of it.

length_rectangle = float(input("Enter the length of rectangle: "))
breadth_rectangle = float(input("Enter the breadth of rectangle: "))
area_rectangle = length_rectangle * breadth_rectangle
print("Area of Rectangle", area_rectangle)


"""Output:
Enter the length of rectangle: 25
Enter the breadth of rectangle: 25
Area of Rectangle 625.0"""