# Take the base and height of a triangle from the user and calculate the area of the triangle.

base_triangle = float(input("Enter the base of the triangle: "))
height_triangle = float(input("Enter the height of the triangle: "))
area = 1/2*(base_triangle * height_triangle)
print("Area of triangle =", area)


"""Output:
Enter the base of the triangle: 17.6
Enter the height of the triangle: 23.7
Area of triangle = 208.56"""