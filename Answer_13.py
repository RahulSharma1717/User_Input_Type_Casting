#  Take the length and breadth of a rectangle and find the perimeter of the rectangle
#     perimeter= 2 * (length + breadth)

length = float(input("Enter length of the rectangle: "))
breadth = float(input("Enter the breadth of the rectangle: "))
perimeter = 2*(length + breadth)
print(f"Perimeter of the rectangle with sides {length} and {breadth} is {perimeter}")


"""Output:
Enter length of the rectangle: 12.4
Enter the breadth of the rectangle: 18.7
Perimeter of the rectangle with sides 12.4 and 18.7 is 62.2"""