#  Take the side of a cube in cm and calculate the volume of the cube.
#      volume= side * side * side

side = float(input("Enter side of cube in cms: "))
volume = side**3
print(f"Volume of the cube is {volume:.3f} cubic cms")


"""Output:
Enter side of cube in cms: 17.48639
Volume of the cube is 5346.881 cubic cms"""